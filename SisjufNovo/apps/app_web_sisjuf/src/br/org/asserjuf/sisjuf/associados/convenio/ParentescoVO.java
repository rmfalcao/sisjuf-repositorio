package br.org.asserjuf.sisjuf.associados.convenio;

/**
 * Classe que encapsula as informações de grau de parentesco com o Associado
 * @author Paulo
 *
 */
public class ParentescoVO {

	private Integer codiogo;
	private String descricao;
	
	public Integer getCodiogo() {
		return codiogo;
	}
	public void setCodiogo(Integer codiogo) {
		this.codiogo = codiogo;
	}
	public String getDescricao() {
		return descricao;
	}
	public void setDescricao(String descricao) {
		this.descricao = descricao;
	}
	
	
	
}
